#include "crazypng.h"

uint32_t	swap_endian(uint32_t value)
{
	return (((value >> 24) & 0xFF) | ((value >> 8) & 0xFF00) | \
		((value << 8) & 0xFF0000) | ((value << 24) & 0xFF000000));
}
